package homeWork;

public class Point {
    int dottX;
    int dottY;

    public Point(int dottX, int dottY) {
        this.dottX = dottX;
        this.dottY = dottY;
    }

    public int getDottX() {
        return dottX;
    }

    public void setDottX(int dottX) {
        this.dottX = dottX;
    }

    public int getDottY() {
        return dottY;
    }

    public void setDottY(int dottY) {
        this.dottY = dottY;
    }
}
